USE `eniwhere_db`;

CREATE TABLE IF NOT EXISTS `addresses` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `postal_code` VARCHAR(20) NULL,
  `country` VARCHAR(100) NOT NULL,
  `state` VARCHAR(100) NOT NULL,
  `city` VARCHAR(100) NOT NULL,
  `neighborhood` VARCHAR(100) NOT NULL,
  `address_line` VARCHAR(255) NOT NULL,
  UNIQUE (`id`),
  UNIQUE (`postal_code`),
  PRIMARY KEY (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `app_users` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `document` VARCHAR(45) NOT NULL,
  `name` VARCHAR(100),
  `email` VARCHAR(100) NOT NULL,
  `phone` VARCHAR(45),
  `username` VARCHAR(45) NOT NULL,
  `user_password` VARCHAR(100) NOT NULL,
  `number` VARCHAR(45),
  `address_id` INT NULL,
  `active` TINYINT NOT NULL DEFAULT 1,
  PRIMARY KEY (`id`),
  UNIQUE (`document`),
  UNIQUE (`email`),
  UNIQUE (`username`),
  FOREIGN KEY (`address_id`) REFERENCES `addresses` (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `devices` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `device_name` VARCHAR(100) NOT NULL,
  `model` VARCHAR(100) NOT NULL,
  `brand` VARCHAR(100) NOT NULL,
  `active` TINYINT NOT NULL DEFAULT 1,
  PRIMARY KEY (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `user_devices` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `user_id` INT NOT NULL,
  `device_id` INT NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`user_id`) REFERENCES `app_users` (`id`),
  FOREIGN KEY (`device_id`) REFERENCES `devices` (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `stores` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(100) NOT NULL,
  `document` VARCHAR(45) NOT NULL,
  `email` VARCHAR(100) NOT NULL,
  `username` VARCHAR(45) NOT NULL,
  `user_password` VARCHAR(100) NOT NULL,
  `number` INT NOT NULL,
  `address_id` INT NOT NULL,
  `subscription_end` DATETIME NOT NULL,
  `analytics` TINYINT NOT NULL DEFAULT 0,
  `active` TINYINT NOT NULL DEFAULT 1,
  PRIMARY KEY (`id`),
  UNIQUE (`document`),
  UNIQUE (`username`),
  FOREIGN KEY (`address_id`) REFERENCES `addresses` (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `store_workers` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(100),
  `username` VARCHAR(45),
  `user_password` VARCHAR(100),
  `email` VARCHAR(100) NOT NULL,
  `store_id` INT NOT NULL,
  PRIMARY KEY (`id`),
  UNIQUE (`username`),
  UNIQUE (`email`),
  FOREIGN KEY (`store_id`) REFERENCES `stores` (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `service_orders` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `user_device_id` INT NOT NULL,
  `worker_id` INT NOT NULL,
  `store_id` INT NOT NULL,
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `completed_at` DATETIME NULL,
  `feedback` INT NULL,
  `warranty` INT NULL,
  `cost` DECIMAL(10,2) NULL,
  `work` DECIMAL(10,2) NULL,
  `status` VARCHAR(100) NULL DEFAULT 'pending',
  `deadline` DATE NULL,
  `problem` VARCHAR(255) NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`user_device_id`) REFERENCES `user_devices` (`id`),
  FOREIGN KEY (`worker_id`) REFERENCES `store_workers` (`id`),
  FOREIGN KEY (`store_id`) REFERENCES `stores` (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `admins` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `username` VARCHAR(45) NOT NULL,
  `user_password` VARCHAR(100) NOT NULL,
  `email` VARCHAR(100) NOT NULL,
  PRIMARY KEY (`id`),
  UNIQUE (`username`),
  UNIQUE (`user_password`),
  UNIQUE (`email`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `order_logs` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `service_order_id` INT NOT NULL,
  `cost` DECIMAL(10,2) NOT NULL,
  `work` DECIMAL(10,2) NOT NULL,
  `status` VARCHAR(100) NOT NULL,
  `deadline` DATE NOT NULL,
  `problem` VARCHAR(255) NOT NULL,
  `log_date` DATETIME NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`service_order_id`) REFERENCES `service_orders` (`id`)
) ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `pictures` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `service_order_id` INT NOT NULL,
  `path` VARCHAR(255),
  PRIMARY KEY (`id`),
  FOREIGN KEY (`service_order_id`) REFERENCES `service_orders` (`id`)
) ENGINE = InnoDB;

